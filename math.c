#include<stdio.h>
#include <math.h>

/**
 * Ceil() function returns the smallest integer value greater than or equal to x
 */
int main()
{
    printf("\n%f",ceil(3.6));

    printf("\n%f",ceil(3.3));

    printf("\n%f",floor(3.6));

    printf("\n%f",floor(3.2));

    printf("\n%f",sqrt(16));

    printf("\n%f",sqrt(7));

    printf("\n%f",pow(2,4));

    printf("\n%f",pow(3,3));

    printf("\n%d", abs(-12));

    return 0;

}